    private void btnBrisanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrisanjeActionPerformed
        if(lstListaD.getSelectedValuesList()==null
            || lstListaD.getSelectedValuesList().isEmpty()){
            JOptionPane.showMessageDialog(getRootPane(),
                "Prvo odaberite djelatnika za brisanje!");
            return;
        }
        
        obradaD.setEntitet(lstListaD.getSelectedValue());

        if(JOptionPane.showConfirmDialog(
            getRootPane(),
            "≈Ωelite li sigurno obrisati " + obradaD.getEntitet().getIme() + " " + obradaD.getEntitet().getPrezime()  + " ?",
            "Brisanje",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE)==JOptionPane.NO_OPTION){
        return;
        }

        try {
            obradaD.delete();
            lstListaD.repaint();
            //ucitaj();
        } catch (ZooException ex) {
            JOptionPane.showMessageDialog(
                getRootPane(),
                ex.getPoruka());
        }

    }//GEN-LAST:event_btnBrisanjeActionPerformed

    private void btnIzmjenaDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmjenaDActionPerformed
        if(lstListaD.getSelectedValue() == null || lstListaD.getSelectedValue().equals(null)) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite ≈æeljenog djelatnika!","Upozorenje",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        obradaD.setEntitet(lstListaD.getSelectedValue());
        var s = obradaD.getEntitet();
        dpr = new DjelatnikPromjena(s.getIme(),s.getPrezime(),s.getIBAN(),s, lstListaD);
        dpr.prikazi();
        
        
        
    }//GEN-LAST:event_btnIzmjenaDActionPerformed

