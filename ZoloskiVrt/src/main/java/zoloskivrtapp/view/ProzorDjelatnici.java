    private void ucitajDjelatnike() {
        
        DefaultListModel<Djelatnik> d = new DefaultListModel<>();
        d.addAll(obradaD.read(txtPretragaD.getText()));
        lstListaD.setModel(d);
        lstListaD.repaint();
        
    }
